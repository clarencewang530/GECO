def get_grid_size(total_len, width):
    return (min(width, total_len), max( (total_len+1)//width, 1 ))

def save_image_grid(img, fname, drange, grid_size):
    lo, hi = drange
    img = np.asarray(img, dtype=np.float32)
    img = (img - lo) * (255 / (hi - lo))
    img = np.rint(img).clip(0, 255).astype(np.uint8)

    gw, gh = grid_size
    img = img[:gw * gh]
    _N, C, H, W = img.shape
    img = img.reshape([gh, gw, C, H, W])
    img = img.transpose(0, 3, 1, 4, 2)
    img = img.reshape([gh * H, gw * W, C])

    assert C in [1, 3, 4]
    if C == 1:
        Image.fromarray(img[:, :, 0], 'L').save(fname)
    if C == 3:
        Image.fromarray(img, 'RGB').save(fname)
    if C == 4:
        Image.fromarray(img[..., :3], 'RGB').save(fname)
    
def save_video(frames, fname):
    frames = [(image.permute(1, 2, 0).cpu().numpy() * 255).astype(np.uint8) for image in frames]
    if len(frames) < 8:
        frames = np.concatenate(frames, axis=0)
        Image.fromarray(frames).save(fname+'.png')
        return
    imageio.mimwrite(fname, np.stack(frames), fps=40, quality=10)

def to_rgb_image(maybe_rgba: Image.Image):
    if maybe_rgba.mode == 'RGB':
        return maybe_rgba
    elif maybe_rgba.mode == 'RGBA':
        rgba = maybe_rgba
        img = np.random.randint(127, 128, size=[rgba.size[1], rgba.size[0], 3], dtype=np.uint8)
        img = Image.fromarray(img, 'RGB')
        img.paste(rgba, mask=rgba.getchannel('A'))
        return img
    else:
        raise ValueError("Unsupported image type.", maybe_rgba.mode)

def unscale_latents(latents):
    latents = latents / 0.75 + 0.22
    return latents

def unscale_image(image):
    image = image / 0.5 * 0.8
    return image